	//@DisplayName("test")
	@Test
	public void testRunNoVolatile() {
		try {
			/*
			 * 可见性问题是因为代码没有给JIT编译器足够的提示而使得其认为状态变量cancelled只有一个线程对其进行访问
			 */
			NoVolatileJob job = new NoVolatileJob();
			Thread t = new Thread(job);
			t.start();
			TimeUnit.SECONDS.sleep(5);
			job.cancel();
			t.join();
			
		} catch (Exception e) {
			log.error("test =====> ", e);
		}
	}
	
