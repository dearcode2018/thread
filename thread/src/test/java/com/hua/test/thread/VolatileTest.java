	class NoVolatileJob implements Runnable {
		// 没有volatile 修饰，运行后的线程一直持有私有值，没有去感知其他线程对它的修改
		private boolean cancelled = false;
		
		/**
		 * @description 
		 * @author qianye.zheng
		 */
		@Override
		public void run() {
			System.out.println("job has been started.");
			// 当前线程无法感知其他线程对cancelled的修改，会导致死循环
			while (!cancelled) {}
			System.out.println("job has been cancelled.");
		}
		
		void cancel() {
			this.cancelled = true;
		}
	}
	
	class VolatileJob implements Runnable {
		// 添加了 volatile 确保了所有线程对某个线程对该字段修改后的可见性
		private volatile boolean cancelled = false;
		
		/**
		 * @description 
		 * @author qianye.zheng
		 */
		@Override
		public void run() {
			System.out.println("job has been started.");
			while (!cancelled) {}
			System.out.println("job has been cancelled.");
		}
		
		void cancel() {
			this.cancelled = true;
		}
	}
	
